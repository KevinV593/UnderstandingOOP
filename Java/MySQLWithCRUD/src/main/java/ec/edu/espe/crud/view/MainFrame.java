    void listar(){
        String sql = "Select * from persona";
        try {
            cn = connection.getConnection();
            st = cn.createStatement();
            rs = st.executeQuery(sql);
            Object[]persona = new Object[3];
            modelo = (DefaultTableModel)tblBaseDeDatos.getModel();
            while (rs.next()) {
                persona[0]=rs.getInt("Id");
                persona[1]=rs.getString("DNI");
                persona[2]=rs.getString("Nombres");
                modelo.addRow(persona);
            }
            tblBaseDeDatos.setModel(modelo);
        } catch (Exception e) {    }
    }
    void agregar() {
        String dni = txtDni.getText();
        String nom=txtName.getText();
        if (dni.equals("")||nom.equals("")){
            JOptionPane.showMessageDialog(null, "Cajas vacias..");
        } else {
            String sql = "insert into persona (DNI, Nombres) values('"+dni+", "+nom+"')";
            try {
                cn = connection.getConnection();
                st = cn.createStatement();
                st.executeUpdate(sql);
                JOptionPane.showMessageDialog(null, "Usuario Agregado...");
                limpiarTabla();
            } catch (Exception e) {}
        }
    }
    void limpiarTabla () {
        for(int i=0; i<=tblBaseDeDatos.getRowCount();i++){
            modelo.removeRow(i);
            i=i-1;
        }
    }
